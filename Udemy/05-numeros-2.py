#FUNCIONES CON NUMEROS

print('---Suma-----')
def suma(a = 0, b = 0):
    print(a + b)

suma(2, 5)
suma(14, 15)
suma(8)

print('---Resta-----')
def restar(a, b):
    print(a - b)

restar(8, 5)

print('---Multiplicacion-----')
def multi(a, b):
    print(a * b)

multi(7, 9)

print('---Division-----')
def division(a, b):
    print(a / b)

division(15, 5)